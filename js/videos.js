const cont = document.getElementById("cont-video"); // Obtener el video por su ID
const video = document.getElementById("videos"); 
cont.addEventListener("mouseover", () => {
  video.play(); // Reproducir el video cuando se pasa el mouse sobre él
});

cont.addEventListener("mouseout", () => {
  video.pause(); // Pausar el video cuando se quita el mouse del objeto
});




